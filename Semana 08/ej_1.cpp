#include <iostream>
using namespace std;

int main()
{
    // FOR
    for(int i = 0; i < 10; i++)
    {
        cout << i << endl;

    }

    cout << " ------------------------ " << endl;

    //WHILE

    int j = 0;

    while(j < 10)
    {
        cout << j << endl;

        j++;
    }


    return 0;
}
